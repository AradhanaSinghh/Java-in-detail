package com.company;

public class First_13 {
    public static void main(String[] args){
        int b=10;
        do{ //do will execute the b value and then checks the condition
            System.out.println(b);
            b++;
        }while(b<5);//here 10 will get printed as in do-while one answer is must for execution

        int a=1;
        do{
            System.out.println(a);
            a++;
        }while(a<5);

        int n=7;
        do{
            System.out.println(n);
            n++;
        }
        while(n<10);
    }
}
